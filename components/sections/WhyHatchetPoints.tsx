function PlatformIcon({ name }: { name: string }) {
  const path = PLATFORM_ICONS[name];
  // Platforms without a verified glyph get a muted monogram tile that fills the
  // same slot as a real logo, so every cell reads as intentional and aligned
  // rather than a loud dot floating next to the brand marks.
  if (!path) {
    return (
      <span
        aria-hidden="true"
        className="bg-elevated text-muted/80 flex size-4.5 shrink-0 items-center justify-center rounded-[3px] text-[0.65rem] leading-none font-semibold"
      >
        {name.charAt(0)}
      </span>
    );
  }
  return (
    <svg
      aria-hidden="true"
      className="size-4.5 shrink-0"
      fill="currentColor"
      role="img"
      viewBox="0 0 24 24"
    >
      <path d={path} />
    </svg>
  );
}

